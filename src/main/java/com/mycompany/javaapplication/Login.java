    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here 
            String username = txtUsername.getText();
            String password = String.valueOf(txtPassword.getPassword());
            

            boolean isValid = UserHandler.loginUser(username,password);
            
            if((username.isEmpty() || password.isEmpty())){
                 JOptionPane.showMessageDialog(null, "Complete the input");
            }
            else{
            if(isValid){
                
                String role = UserCredentials.GetRoleUser(username);
                if(role.equals("student")){
                    //reset the counter
                counter = 0;
                
                 String fname = UserCredentials.GetFirstName(username);
                 String lname = UserCredentials.GetLastName(username);
               
                
                // to close the login form and direct to dashboard
                Login nF = new Login();
                nF.setVisible(false);
                dispose();
                
                //Display the Dashboard Form
                loading loadingFrame = new loading(fname, lname);
                loadingFrame.setVisible(true);
                loadingFrame.pack();
                }
                else{
                     JOptionPane.showMessageDialog(null, "This account is not user");
                     counter += 1;
                      if(counter==3){
                    try{
                        //JOptionPane.showMessageDialog(null,"3 invalid input. \n1 minute break");
                        Break b = new Break();
                        b.setVisible(true);
                        
                        //program will stop for a minute
                        //Thread.sleep(60000);
                         //JOptionPane.showMessageDialog(null,"1 minute done, input again");
                        // reset the counter
                        counter = 0;
                    }
                    catch(Exception e){
                        System.out.println("Interrupted");
                    }
                    
                   
                }
                else{
                    JOptionPane.showMessageDialog(null, "Invalid user");
                }
                }
               
                
            }
            else{
                counter+= 1;
                if(counter==3){
                    try{
                        //JOptionPane.showMessageDialog(null,"3 invalid input. \n1 minute break");
                        dispose();
                        Break b = new Break();
                        b.setVisible(true);
                        
                        //program will stop for a minute
                        //Thread.sleep(60000);
                         //JOptionPane.showMessageDialog(null,"1 minute done, input again");
                        // reset the counter
                        counter = 0;
                    }
                    catch(Exception e){
                        System.out.println("Interrupted");
                    }
                    
                   
                }
                else{
                    JOptionPane.showMessageDialog(null, "Invalid user");
                }
            }
    }//GEN-LAST:event_loginBtnActionPerformed
    }
    
